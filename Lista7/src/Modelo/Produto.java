package Modelo;

public class Produto {

	private String nome;
	private Long Codbarras;
	
	
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public Long getCodbarras() {
		return Codbarras;
	}
	public void setCodbarras(Long codbarras) {
		Codbarras = codbarras;
	}
	
	
}
